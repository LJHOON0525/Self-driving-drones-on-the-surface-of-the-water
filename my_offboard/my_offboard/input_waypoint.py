    (35.97018284414415, 126.5720277,        10.0),  # F7
    (35.97011077207208, 126.5720277,        10.0),  # F8
    (35.9700387,        126.5720277,        10.0),  # F9
    (35.96996662792793, 126.5720277,        10.0),  # F10
    (35.96989455585586, 126.5720277,        10.0),  # F11
